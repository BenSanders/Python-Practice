n = 1
while n < 3:
	print(n - 2)
	n = n + 1
	
# Correct
# 	
# 	The loop variable n is initialized to 1. Each time the loop body executes, n - 2 is printed, then n is incremented. The loop body executes for n assigned with 1, and 2. The loop ends when n is 2 because the while loop condition is n < 3.

